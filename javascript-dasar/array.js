// TIPE DATA ARRAY
// Array digunakan untuk menyimpan banyak nilai dalam satu variabel

const array = [1, true, 'tiga', 4, undefined];

// Tambah dibelakang
array.push(6);

// Tambah diawal
array.unshift(0);

// Menghapus dibelakang
array.pop();

// Menghapus diawal
array.shift();

console.log(array);
